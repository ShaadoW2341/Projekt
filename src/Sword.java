public class Sword {
    private int swordPower;
    private int swordTier;

    public Sword(int swordPower, int swordTier) {
        this.swordPower = swordPower;
        this.swordTier = swordTier;
    }

    public int getSwordPower() {
        return swordPower;
    }

    public int getSwordTier() {
        return swordTier;
    }

}
