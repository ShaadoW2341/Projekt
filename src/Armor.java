public class Armor {
    private int armorPower;
    private int armorTier;

    public Armor(int armorPower, int armorTier) {
        this.armorPower = armorPower;
        this.armorTier = armorTier;
    }

    public int getArmorPower() {
        return armorPower;
    }

    public int getArmorTier() {
        return armorTier;
    }

}
